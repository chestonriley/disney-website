            <div class="para-content">
                <p>Okay...so you're thinking by now, "Is there just anything that you didn't like about going to Disney World?</p>
                <p>Well, there's really not much, if you must know.  But there is a bad side to everything, and there are certainly some things that we have learned over the years that we would avoid in the future.  None of them are show-stoppers, but if you are going to maximize your time (and MONEY) while at your stay, then I would recommend that you avoid certain things.</p>
                <p>Of course, my presumptions in most of these circumstances are they are generally like me:  A bit of an introvert, not willing to waste time and money, and certainly type-A to the core.  If you are more easy going and willing to take the lemons and make lemonade, then by all means ignore me.</p>
                <p>But for the rest of us that have a tendency to bring our fast-paced, no-nonsense, non-money-wasting style along with you on vacation, then these tips are for you.  Do realize, though, that bringing your lifestyle along with you on vacation is the one thing that you should be trying to avoid, though.  So take into account that pulling you out of your usual "make the world around you line up with what you want" mindset is honestly one of the keys to having a great time.</p>
                <p>But enough will all of that theory.  Now on to the don't do's:</p>
                <ul id="do_not_do_list">
                    <li>First and foremost, <u>I would avoid the T-Rex Cafe.</u>  We've been there twice now, and it's been really bad both times.  There have been a few issues that have just made it a bad experience.  First, we had under-cooked food issues BOTH times.  After a long wait to even get the food (because we had order "lost" issue BOTH times as well), getting a plate of nearly-raw cuisine was enough to make me hit the roof.  All of this wouldn't have been bad for a good price, but I think that they figure you are willing to pay for the dinosaur ambiance, so the price-to-food-quality ratio is easily one of the worst I've seen at Walt Disney World.  Just avoid this one, until they can get their stuff straight.  Maybe look for the sign "Under New Management"?</li>
                    <li>For goodness sake, <u>don't go to the parks on your first full day!!!</u>  This was something that we figured out the first year... the hard way.  Honestly the pressure of having paid for the ticket, assuming that you would have "fun" on the first day and then being so tired and frankly not yet unwound was just too far to much to maintain sanity.  It's just not a good idea to spend all of the energy traveling (which is stressful) and expect that you are going to unwind by going to the parks.  Going to the parks is NOT relaxing.  Take some time to have a down-day before you go jumping into the park experiences.</li>
                    <li><u>Don't get your hopes up about crowd levels.</u>  The crowd levels vary (obviously) throughout the year.  But frankly it's never not busy.  If you think that you might end up with a day that is somewhat devoid of large crowds, then you are doomed for disappointment.</li>
                    <li><u>Don't try to save a bunch of money.</u>  I can't stress this one enough.  Disney was DESIGNED to get you to spend money.  You are surrounded by the "buy-me!!!" temptations everywhere you go.  You will most likely end up feeling like a failure if you don't give in sometimes.  It's just how the place was designed.  Spend a lot of time before you go researching how much you will spend at restaurants, parks, shopping, etc.  Then... gross that up by about 20%, and make that your budget.  It's okay to have a budget for sure, just don't have your budget be centered around saving money.  Have it be about not spending too much money.</li>
                    <li><u>Don't try to do everything.</u>  Seriously, you could probably go to Disney World 20 times and still not see or do everything.  Your best bet is to make a loose plan of what you want to do ahead of time, and just plan to not even get THAT much done. Disney is a great place to relax as long as you allow yourself to do it.  Don't try to do too much.</li>
                    <li>More to come!!!</u></li>
                </ul>
                <p>Disney World doesn't have too many "don't do's", which is a blessing.  But the fact is that you generally get what you pay for, and everything is expensive!!!</p>

