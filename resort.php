            <div class="para-content">
                <p><a href="http://www.wyndhambonnetcreek.com/">The Wyndham Bonnet Creek Resort</a> is perhaps one of the best resorts on the Disney property.  As a matter of fact, I’ve had a good friend and Disney travel agent say as much, and he has stayed at Disney well over 20 times, and is a Disney travel agent.</p>
                <p>The reviews for Bonnet Creek are generally incredible.  People really enjoy the cleanliness, the amenities, and (for those of us that rent) the good balance of price/quality/location.  Some of the bad reviews are generally on the amount of time that it takes to check-in.  We haven’t personally experienced any issues related to long check-in times, but some folks have been generally displeased with that experience.  So, after a long day of travel, just be aware that the wait time to check in could be up to 45 minutes.  Once you are done with check-in, however, you won’t be dealing with the front desk much after that.  So try to relax during the onboarding process, and realize that it’s not forever.</p>
                <p>The resort is organized in 6 towers, on Village Center, and one hotel <a href="http://www.wyndhamgrandorlando.com/">(The Wyndham Grand Hotel)</a>.  Upon checking in, you will be assigned a room in a particular tower.  Be sure to push in requesting your particular wants/needs.  For instance, you can have either a water view or a parking lot view.  But do realize that, in some of the towers, the parking lot views are high enough to see the fireworks at night.  In addition, a water view might put you close to a swimming pool, and right in ear-shot of night-time swimming while you are trying to go to sleep.  So, make an educated choice about what will fit you for the week.  You may not end up getting what you want, but one thing’s for sure:  You definitely won’t get it if you don’t ask.</p>
                <p>Bonnet Creek is a timeshare, and they WILL know that you are renting from someone else.  So they will try to sign you up for a tour by waving free Disney park tickets (or something similar) at you.  If you enjoy getting stuck with the sales pitch, by all means, do.  Just know that it’s not a condition of your stay, and feel free to decline.  Michelle and I DON’T “indulge” in that fun.  
Check-in will be in the Village Center (the main office).  At that time they will give you a parking pass.  None of us should need that except if you do rent a car.  In that case, just go back down to the main office to get a parking pass.  It IS necessary to get on and off the Resort property.</p>
