            <div class="para-content">
                <p>Packing for Disney can be a bit on the stressful side, especially if you are not a planner.  The good news is that there is a Walmart just minutes away from the property in case that you forget something.  The problem is, that if you forget too many things, your lack of planning will definitely cost you.</p>
                <p>There are a TON of websites out there that will make recommendations on what you should bring, but here’s our take.</p>
                <p>First of all, the name of the game should be to travel light.  The travel day is a bit exhausting, anyway, and the last thing you want is to start your vacation week by lugging around a bunch of stuff that you may not even use.  So think realistically about what you might purchase (like flip-flops/etc.) when you get to Orlando.</p>
                <p>In addition, keep in mind that your room will have a washer and dryer.  We end up doing laundry everyday, so don’t be surprised if you do as well.  There are only a few Disney restaurants that have a dress code, and we are not going to any of them. </p>
                <p>I would say to consider the following list of items:</p>
                <ul id="pack_list">
                    <li>Easy clothes (shorts, t-shirts, underwear, socks)</li>
                    <li>One set of “nice” clothes</li>
                    <li>Minimal toiletries.  There are no plans for anyone to participate in a beauty contest this year.</li>
                    <li>Comfy shoes for the park</li>
                    <li>Pool shoes</li>
                    <li>Swimsuit and goggles</li>
                    <li>Computer, iPad, etc.  DON'T FORGET THE CHARGER!!!</li>
                    <li>Your Disney tickets and Magic Bands</li>
                    <li>Sunglasses</li>
                </ul>
                <p>Finally, we usually wear our cold-weather clothes during the travel.  Jeans and a sweatshirt do nicely.  The average rainfall in May in Orlando is small, but there could be a “chilly” day or two.  Last year we did wear our colder-weather clothes one morning, so it is worth having it.  Don’t get sucked into thinking that Florida will be magically warm… Disney, unfortunately, doesn’t have that much “pixie-dust”.</p>

