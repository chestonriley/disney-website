                <p>Okay, now time to go to the parks!  But hold on just a second… what do I need to know?  First of all, the beginning of May is a lot better for crowds than the middle of June, but the parks are ALWAYS crowded.  Mentally preparing for lots of people is essential.  In addition, Disney draws tourists from all over the world.  Some cultures have a corner-stone of good manners, and others do not.  So be prepared for pushy people and try not to take it personally.</p>
                <p>Getting to Magic Kingdom is also a gigantic headache.  If you go to the “main entrance”, that’s only the beginning.  You will then either have to ride the monorail or the ferry to the actual park entrance.  Both ways keep moving, but just prepare for about 30-40 minutes of additional time to actually get into the park.  We are actually going to try to take a taxi to the Disney Contemporary Resort this year, and then walk to Magic Kingdom.  We’ve not tried this before, but it seems like it will work.</p>
                <p>Next, consider bringing a single backpack for you and your family, equipped with several items that are essential:</p>
                <ul id="park_list">
                    <li>A poncho for everyone.  May is not a wet month in Florida, but it’s good to be prepared.  You might want to consider and extra one to drape over a stroller as well.</li>
                    <li>A water bottle.  The days get hot and humid, and keeping hydrated is a must for continual magical fun.  But water (along with everything else) costs.  You might consider bringing a water bottle and filling up at the fountains.</li>
                    <li>Fan/water mister.  You can pick one of these up at <a href="http://www.walmart.com/ip/O2Cool-Deluxe-Misting-Fan-Teal/43980198?action=product_interest&action_type=title&item_id=43980198&placement_id=irs-106-t1&strategy=PWVUB&visitor_id&category=&client_guid=603d4b96-922d-418c-aa50-fb3bc26dfe7c&customer_id_enc&config_id=106&parent_item_id=34416185&parent_anchor_item_id=34416185&guid=65832f76-b295-4364-a9f4-612ee1bc4516&bucket_id=irsbucketdefault&beacon_version=1.0.1&findingMethod=p13n">Walmart</a> for a LOT cheaper than you will spend in the park.  But this is nearly a must-have.  The kidos will get hot, and the instant cooling for them will be welcomed.</li>
                    <li>Magic Bands, tickets, wallet with credit cards and some cash, and definitely an ID card.</li>
                    <li>Mobile phone.  Definitely make sure your phone is fully charged at the beginning of the day.</li>
                    <li>Hat or visor and sunglasses.  Do NOT forget these for the kids.  The Florida sun is bright and hot!</li>
                    <li>Snacks.  It’s okay to bring some small snacks into the park.  Go ahead and have some on hand.</li>
                    <li>Sunscreen.  This should almost be a given.  And this ain't Holiday World... no free sunscreen for all.</li>
                    <li>Clothes.  Consider and extra change of clothes for the kids.  There are several places where playing in water is possible.  You might even consider bringing (or wearing) a swimsuit.</li>
                    <li>Zip-lock bags.  Helps keep things organized and dry in your backpack.</li>
                    <li>Wet-wipes.  Mommies know all about this.</li>
