                <p>If you use your mobile phone to take video, it will RAPIDLY lose battery life for 2 reasons.  First, filming itself eats up more battery than just being on standby.  Secondly (at least with iPhone), the phone will continually try to sync-up video with “the cloud”.  The constant attempts will rapidly drain your battery.  So set yourself up for filming success by changing the setting on your phone to prevent it from trying to sync up.  Also, consider buying a mobile charging pack so that you can recharge without having to find an outlet and waiting for your phone to do it’s business.</p>
