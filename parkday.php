                <p>Your magic band is your key to access.  It will get you into the park (once activated).  In addition, you will be required to scan it to access your pre-arranged FastPass+ appointments.  Once you have exhausted (or canceled) your existing FastPass’s, you can get more.  You can either go to the ride and scan your magic band and receive a return time, OR you can visit a FassPass kiosk to arrange a FastPass at a particular time.  Remember that you can only have ONE FastPass reservation at a time (except for the previously reserved FastPass+), so choose wisely.  Finally, when showing up for your ride experience, ensure that you meet your appointed time.  You can’t get through early, and being late will cancel your FastPass.  Make sure that you find the FastPass entrance as well.  Don’t get in the long line… that’s why you got the FastPass in the first place!</p>
                <p>Disney does not make it difficult to buy anything.  Main Street USA in Magic Kingdom is lined with shops and vendors, and designed to lure in the adults through the excited children.  Be prepared with your “no” strategy ahead of time.  We typically tell Luke that he can buy 2 things while at Disney, and that he should take his time to identify things.  In addition, if your little one sees something in the park, chances are that it is available at Downtown Disney.  Promising to get it later is a great because it prevents you from carting something around in the park all day, and perhaps they’ll forget about it (yeah right!) or chose something else later.</p>
                <p>The heat and humidity WILL drain you, kinda like living in the Ohio Valley.  Make sure that you get a good night’s sleep the day before the park.  Also, think about what your afternoon strategy will be.  Consider going back to the resort.  You can also take a break by riding the monorail (for free) to a Disney Resort (like the Polynesian or the Grand Floridian) and finding a place to sit and get something the drink and relax for a while.  Don’t worry…you don’t need to scan your magic band to visit a resort.  The important thing is that you formulate your plan for afternoon cooling off, nap and relaxation time.  This is especially important if you plan to go back to the park later in the day.  Charging through is a recipe for kindergartener-meltdown disaster.</p>
                <p>The parades are a double-edged sword as well.  If you plan to watch the parades, by all means keep track of the time and get a good viewing spot ahead of time.  If, however, you are not, you can get stuck trying to wind your way past them.  At Magic Kingdom, Main Street USA will be shut down periodically for a parade.  If you find Main Street between you and your next FassPass during that time, you will spend a lot of extra time trying to get around the parade.</p>
                <p>Being in the parks is great fun, but being not being prepared can quickly drain all the magic away.  Visit a few websites that have tips/tricks as well!</p>
