            <div class="para-content">
                <p>One of my favorite things to look forward to is all of awesome restaurants at Disney World.  There is somewhere north of 140 restaurants at Walt Disney World, and about half of those are available for advanced reservations.  Advanced, of course, is an understatement.</p>
                <p>I don't really like to plan out the Disney vacation down to the daily activities, but the facts here are simple.</p>
                    <ul>
                        <li>Disney World restaurants are available for reservations 6 months out.  That's right... 6 months!!!  and</li>
                        <li>The restaurant reservations will start to fill up very quickly even at 180 days in advance.</li>
                    </ul>
                <p>So if you are planning to get a seat at one of the restaurants that you found online that you just "gotta have", you are going to need to start reserving your times at the 180-day out mark.</p>
                <h2 id="dining_options">My Favorite Dining Options</h2>
                <p>Again, there are more than 140 places to eat at Disney World.  The cuisine options are wide-ranging, and rarely will you have an experience where the food or the service is not top-notch.  We've been there for several years in a row now, and have had the opportunity to both be repeat customers at certain restaurants, and tried out some new ones.  My personal favorites are:</p>
                <ul id="dining_list">
                    <li><a href="https://disneyworld.disney.go.com/dining/disney-springs/raglan-road-irish-pub-and-restaurant/"><p class="restaurant_title">Raglan Road Irish Pub in Disney Springs.</p></a>  This is heaven on earth for a Irishman.  This incredible restaurant boasts traditional Irish favorites, and the most amazing dish in the whole world:  Sod the Stew.  In addition, the Irish dancers are some of the best in the world, and dance along to the awesome tunes of live Irish music.  With a Guiness in your hand, there is nothing better than being here. </li>
                    <li><a href="https://disneyworld.disney.go.com/dining/magic-kingdom/liberty-tree-tavern/"><p class="restaurant_title">Liberty Tree Tavern in Magic Kingdom.</p></a>  What a gem!  This must be the best kept secret in all of Disney World.  The quaint facade on the building disguises some truly wonderful food.  The New England Pot Roast is absolutely insanely good.  You see a lot of people walking right by this place during the lunch hour.  I say... keep on walking, more for me!</li>
                    <li><a href="https://disneyworld.disney.go.com/dining/animal-kingdom-villas-kidani/sanaa/"><p class="restaurant_title">Sanaa in Animal Kingdom Lodge.</p></a>  What a great adventure for the family.  This African themed restaurant sits right next to the outdoor animal habitat of Animal Kingdom Lodge.  If you are lucky enough to get a window seat, you can have a great time viewing the African animals as they dine.  The food is very good, and I definitely recommend the Indian Style Bread Service appetizer.  The awesome naan served with a full spread of chutney's is superb.</li>
                    <li><a href="https://disneyworld.disney.go.com/dining/wilderness-lodge-resort/whispering-canyon-cafe/"><p class="restaurant_title">Whispering Canyon Cafe in Disney's Wilderness Lodge.</p></a>  Ribs ribs ribs.  This place features pork ribs that are slow roasted from the wee hours of the morning.  They literally melt in your mouth.  Go there...get them.  But be weary of asking for ketchup, because the performing staff LOVE to encourage the whole restaurant to bring their's to your table.  You'll end up with a whole table filled with ketchup...but how fun!</li>
                    <li><a href="https://disneyworld.disney.go.com/dining/epcot/san-angel-inn-restaurante/"><p class="restaurant_title">San Angel Inn Restaurant in Epcot.</p></a>  Duck in from the hot and enjoy a little Mexican food.  Don't look for burritos on the menu, as this place is authentic.  Dine inside the cool (and dark) Aztec pyramid in Epcot's Mexico pavilion by candlelight.  It's a great way to take a break from the crowds.</li>
                    <li><a href="https://disneyworld.disney.go.com/dining/boardwalk/flying-fish-cafe/"><p class="restaurant_title">The Flying Fish Cafe at Disney's Boardwalk.</p></a>  This is your splurge, as it will definitely cost you.  But if you want some really great seafood in a boardwalk atmosphere, there's no better place.</li>
                    <li><a href="https://disneyworld.disney.go.com/dining/contemporary-resort/california-grill/"><p class="restaurant_title">California Grill at the Contemporary Resort.</p></a>  This one will cost you, too.  But the food doesn't disappoint.  Perhaps more incredible is the "balcony" area on the sides of the restaurant.  These terraces run the full length of the roof of the resort, and diners can come back later in the evening and watch the Magic Kingdom fireworks from the roof.  There's no better place to watch!</li>
                    <li><a href="https://disneyworld.disney.go.com/dining/animal-kingdom/yak-and-yeti-restaurant/"><p class="restaurant_title">Yak and Yeti at Animal Kingdom.</p></a>  There are only three "sit down" restaurants in Animal Kingdom, and this is one of them.  It's a nice place for a park lunch out of the crowds, and the Asian food will knock your socks off.  Centrally located, it's a convenient place to recharge before tackling the rest of the day.</li>
                    <li><a href="https://disneyworld.disney.go.com/dining/epcot/coral-reef-restaurant/"><p class="restaurant_title">Coral Reef Restaurant in Epcot.</p></a>  Don't blink or you'll miss it.  Around the back-side of the aquariums at Epcot you'll find this seafood restaurant that features live fish and sharks as your entertainment.  The restaurant sits right at the bottom of the shark tanks (OUTSIDE the tank, of course), and gives diners an opportunity to view some sealife while eating some, too.</li>
                </ul>

                <p>The cold hard facts is that going to Disney World will just plain cost you, and dining out is no exception to that rule.  But the upside is that Disney does not disappoint.  This theme park food rivals some of the best restaurants in the world, and it's worth coming to the parks just for the incredible food.</p>
                <p>So as long as your are willing to spend the money, Disney will make it well worth it for the food that you will get.  Plan ahead in selecting your restaurants, and make your reservations EARLY.</p>

